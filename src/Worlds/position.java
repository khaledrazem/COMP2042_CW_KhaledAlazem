package Worlds;

/**
 * This class is a definition of the coordinate positions for objects 
 * @author khaled
 *
 */
public class position {

	public int x;
	public int y;
	
	/**
	 * Constructor for class
	 * @param x x coordinate
	 * @param y Y coordinate
	 */
	public position(int x,int y) {
		this.x=x;
		this.y=y;
	}
}
